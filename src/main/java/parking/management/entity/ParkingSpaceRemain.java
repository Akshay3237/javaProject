package parking.management.entity;

public class ParkingSpaceRemain {
	private int NumberOfAutos;
	private int NumberOfCars;
	private int NumberOfTrucks;
	private int NumberOfBikes;
	public int getNumberOfAutos() {
		return NumberOfAutos;
	}
	public void setNumberOfAutos(int numberOfAutos) {
		NumberOfAutos = numberOfAutos;
	}
	public int getNumberOfCars() {
		return NumberOfCars;
	}
	public void setNumberOfCars(int numberOfCars) {
		NumberOfCars = numberOfCars;
	}
	public int getNumberOfTrucks() {
		return NumberOfTrucks;
	}
	public void setNumberOfTrucks(int numberOfTrucks) {
		NumberOfTrucks = numberOfTrucks;
	}
	public int getNumberOfBikes() {
		return NumberOfBikes;
	}
	public void setNumberOfBikes(int numberOfBikes) {
		NumberOfBikes = numberOfBikes;
	}
	
}
